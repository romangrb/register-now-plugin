( function ( $ ) {
	$( document ).ready( function () {
		//Require post title when adding/editing Project Summaries
		$('body').on( 'submit.edit-post', function () {
			// If the title isn't set
			if ( $( "#title" ).val().replace( / /g, '' ).length === 0 ) {
				if ($('#wpbody-content').find('.post-title').length<1) {
					$('#wpbody-content').prepend('<div class="notice notice-warning post-title"><p>The title is required it was autogenerated. Please change it depending of event name. </p></div>');					
					$("#title").val('Event - ' + TribeTickets.auto_generated_post_title);	
				}
				// Hide the spinner
				$( '#major-publishing-actions .spinner' ).hide();
				// The buttons get "disabled" added to them on submit. Remove that class.
				$( '#major-publishing-actions' ).find( ':button, :submit, a.submitdelete, #post-preview' ).removeClass( 'disabled' );
				// Focus on the title field.
				$( "#title" ).focus();
				return false;
			}
		});
		
		$('body').trigger('submit.edit-post');
	});
}( jQuery ) );